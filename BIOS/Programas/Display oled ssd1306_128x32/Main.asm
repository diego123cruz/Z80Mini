#include "../Z80MiniAPI.asm"

; Display oled SSD1306 128x32 ou 128x64
oled    .equ    $78

    .org $8000
    
    

inicio:

    ; display on
    LD a, $AF
    CALL send_cmd
    
    ; set display normal
    LD a, $A6
    CALL send_cmd
    
    ; Page mode
    LD a, $02
    CALL send_cmd
    
    ; Charge Pump
    LD a, $8d
    CALL send_cmd
    LD a, $14
    CALL send_cmd
    

    ; 128x32 - Remover para 128x64
    LD a, $a8
    CALL send_cmd
    LD a, $1f
    CALL send_cmd
    
    ; 128x32 - Remover para 128x64
    LD a, $da
    CALL send_cmd
    LD a, $02
    CALL send_cmd

    ; horizontal
    LD a, $20
    CALL send_cmd
    LD a, $00
    CALL send_cmd
    
    ; col
    LD a, $21
    CALL send_cmd
    LD a, 0
    CALL send_cmd
    LD a, 127
    CALL send_cmd
    
    ; row
    LD a, $22
    CALL send_cmd
    LD a, 0
    CALL send_cmd
    LD a, 3
    CALL send_cmd
    
    
    
    call delay_50ms
    
    ;==================

    ; Teste carregamento de imagem por partes de 128x8

    ld b, 128
    ld hl, z1
img:
    ld a, (hl)
    push bc
    push hl
    call send_data
    pop hl
    pop bc
    inc hl
    djnz img


    ld b, 128
    ld hl, z2
img1:
    ld a, (hl)
    push bc
    push hl
    call send_data
    pop hl
    pop bc
    inc hl
    djnz img1
    
    
    ld b, 128
    ld hl, z3
img2:
    ld a, (hl)
    push bc
    push hl
    call send_data
    pop hl
    pop bc
    inc hl
    djnz img2
    
    
    ld b, 128
    ld hl, z4
img3:
    ld a, (hl)
    push bc
    push hl
    call send_data
    pop hl
    pop bc
    inc hl
    djnz img3
    
    

loop:
    CALL keyboardWaitA
    call send_data
    JP loop

    

delay_50ms:
    LD DE, $0032
    CALL delay
    Ret



send_cmd:
    PUSH AF
    LD A, oled
    CALL I2C_Open
    LD A, $00 ; Commands
    CALL I2C_Write
    POP AF
    CALL I2C_Write
    CALL I2C_Close
    ret


send_data:
    PUSH AF
    LD A, oled
    CALL I2C_Open
    LD A, $40 ; Dados
    CALL I2C_Write
    POP AF
    CALL I2C_Write
    CALL I2C_Close
    ret
    
    

